package com.model.aldasa.entity;

import java.util.Date;

public class Simulador {

	private String nroCuota;
	private Double inicial, cuotaSI, interes, cuotaTotal;

	
	
	
	public String getNroCuota() {
		return nroCuota;
	}
	public void setNroCuota(String nroCuota) {
		this.nroCuota = nroCuota;
	}
	public Double getInicial() {
		return inicial;
	}
	public void setInicial(Double inicial) {
		this.inicial = inicial;
	}
	public Double getCuotaSI() {
		return cuotaSI;
	}
	public void setCuotaSI(Double cuotaSI) {
		this.cuotaSI = cuotaSI;
	}
	public Double getInteres() {
		return interes;
	}
	public void setInteres(Double interes) {
		this.interes = interes;
	}
	public Double getCuotaTotal() {
		return cuotaTotal;
	}
	public void setCuotaTotal(Double cuotaTotal) {
		this.cuotaTotal = cuotaTotal;
	}	
	
	
}
